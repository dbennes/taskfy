<script>
    // Adicione um listener de evento de clique para cada botão
    document.getElementById('btn-raf').addEventListener('click', function() {
        fetchData('RAF'); // Envie 'RAF' como parâmetro para identificar o tipo de dados a serem buscados
    });

    document.getElementById('btn-rif').addEventListener('click', function() {
        fetchData('RIF'); // Envie 'RIF' como parâmetro para identificar o tipo de dados a serem buscados
    });

    // Função para enviar uma solicitação AJAX para o backend (PHP)
    function fetchData(type) {
        var xhr = new XMLHttpRequest();
        xhr.open('POST', 'php/scripts/baixar_relatorios.php', true);
        xhr.setRequestHeader('Content-Type', 'application/x-www-form-urlencoded');
        xhr.onreadystatechange = function() {
            if (xhr.readyState == 4 && xhr.status == 200) {
                // Redirecionar para a página PHP que gera o arquivo
                window.location.href = 'php/scripts/baixar_relatorios.php?type=' + type;
            }
        };
        // Envie o tipo de dados para o servidor
        xhr.send('type=' + type);
    }
</script>
